/* USER CODE BEGIN 0 */
void system_ledBlink()
{
    static uint16_t nextChangeTime = 0;
    static GPIO_PinState pinState = GPIO_PIN_SET;

    if (millis() > nextChangeTime)
    {
#if LED_BLINK_VERSION

        nextChangeTime = millis() + 250;
        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, pinState);
        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, pinState);
        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, pinState);
        HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, pinState);
        pinState = !pinState;

#else
        uint16_t pinNum = GPIO_PIN_12;
        static uint8_t count = 0;
        nextChangeTime = millis() + 100;

        switch (count % 4)
        {
        case 0:
            pinNum = GPIO_PIN_12;
            break;
        case 1:
            pinNum = GPIO_PIN_13;
            break;
        case 2:
            pinNum = GPIO_PIN_14;
            break;
        case 3:
            pinNum = GPIO_PIN_15;
            pinState = !pinState;
            break;
        default:
            break;
        }

        count++;
        HAL_GPIO_WritePin(GPIOD, pinNum, pinState);
#endif
    }
}
